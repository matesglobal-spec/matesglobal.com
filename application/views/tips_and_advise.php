               <h3>
                 How to prepare for interview?
               </h3>
               <p>Easy tips and guidelines are here to follow at a glance which can help in succeeding.</p>
                   <ol>
                       <li><a >DO YOUR HOMEWORK: Know about the company you are applying for; this will help you understand what is expected of you, as well as how well you would be able to contribute to the organisation.</a></li>

                       <li><a >ARRIVE ON TIME: Never be late to the interview. Your first impression should be the best. If need be, navigate the route beforehand and leave home well in advance. This way, you will avoid unexpected hinderances or traffic issues.</a></li>
                       <li><a >BE PREPARED: You should be well prepared for the interview and you should be able to reply with confidence. Practicing mock interviews is the best way to prepare.</a></li>
                       <li><a >APPROPRIATE INTERVIEW ATTIRE: Make sure you are well-groomed and are wearing smart clothing such as suits for men and professional wear for women.</a></li>
                       <li><a >BE POSITIVE AND ENTHUSIASTIC: Be positive and enthusiastic! Focus on your strengths, abilities, experiences and how the skills you possess can help the organisation.</a></li>
                       <li><a >REFERENCES: Provide at least 2 references from people such as former managers, colleagues or senior people with whom you had worked. Seek permission before giving the persons information. Reference checks give an employee confidence that the candidate possesses the rights skills and attitude for the offered position.</a></li>
                       <li><a >DOCUMENTS: Copies of your resume and any identification proof of documents should be taken to the interview, just in case.</a></li>
                       <li><a >PARTICIPATE: If given the chance, show that you are interested and ask questions which are related to the business or the industry. This shows that you have knowledge and a curious mind.</a></li>
                       <li><a >FOLLOW-THROUGH: Make follow up contact with the interviewer; send a hand-written note or friendly email response thanking for the time and reminding them that you are still interested in the job position.</a></li>
                       <li><a > RESUME WRITING: Consider professional help in writing your resume. Also, make sure it is continually updated, and consider having career/industry specific resumes with customised cover letters.</a></li>
